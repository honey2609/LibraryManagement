    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
      try 
      {
       String a=t10.getText();
       String b=t1.getText();
       String c=t2.getSelectedItem().toString();
       String d=t3.getText();
       String e=t4.getSelectedItem().toString();
       String f=t5.getText();
       String g=t6.getText();
       String h=t7.getText();
       
       Class.forName("com.mysql.jdbc.Driver");   
       Connection con=DriverManager.getConnection("jdbc:mysql:///library","root","");
       PreparedStatement smt=con.prepareStatement("insert into student_info values(?,?,?,?,?,?,?,?,?)");      
       smt.setString(1,a);
       smt.setString(2,b);
       smt.setString(3,c);
       smt.setString(4,d);
       smt.setString(5,e);
       smt.setString(6,f);
       smt.setString(7,gender);
       smt.setString(8,g);
       smt.setString(9,h);
      
       
       smt.executeUpdate();
       JOptionPane.showMessageDialog(this,"Record Inserted","",JOptionPane.INFORMATION_MESSAGE); 
       t1.setText("");
       t3.setText("");
       t5.setText("");
       t6.setText("");
       t7.setText("");
      } 
      catch(Exception e)
      {
          System.out.println(e);
         
      }
    }//GEN-LAST:event_submitActionPerformed

