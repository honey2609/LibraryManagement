         String h=t8.getText().toUpperCase();
     
         Class.forName("com.mysql.jdbc.Driver");   
         Connection con=DriverManager.getConnection("jdbc:mysql:///library","root","roh##46705R");
         if(status==1)
           { PreparedStatement smt1=con.prepareStatement("delete from book where book_id=?");
             smt1.setString(1,a);
             smt1.executeUpdate();
             
           }
         PreparedStatement smt=con.prepareStatement("insert into book(book_id,book_category,pages,author,price,publisher,book_name)values(?,?,?,?,?,?,?)");
         smt.setString(1,a);
         //smt.setString(2,b);
         smt.setString(2,c1);
         smt.setString(3,d);
         smt.setString(4,e1);
         smt.setString(5,f);
         smt.setString(6,g);
         smt.setString(7,h);
         
         smt.executeUpdate();
